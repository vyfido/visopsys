      <td>VISOPSYS 0.7 PREVIEW<br>
      (04/02/2008)<p>
      <a href="../img/screenshots/screenshot6.jpg">
<img border="1" src="../img/screenshots/thumb-screenshot6.jpg" align="left" width="154" height="115"></a>A 
preview of the upcoming 0.7 release is now available.&nbsp; This new release 
offers an <a href="preview.php#newlook">updated look</a> and a number of new features, including JPEG image 
support, image resizing, 64-bit disk support, UDF (DVD) filesystem support, and GPT partition table support, as well as lots of 
<a href="preview.php#icons">new icons</a>, <a href="preview.php#wallpaper">wallpaper images</a>, and 
file browsing functionality. New <a href="preview.php#admin">administrative applications</a> and functionality 
have been added, and the ATA/IDE driver has been enhanced, including the ability 
to better support backwards-compatible SATA controllers. FPU context saving has 
been improved, and a number of tweaks and bug fixes are also included.<br>
